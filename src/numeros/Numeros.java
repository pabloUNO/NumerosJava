package numeros;

public class Numeros {

	public int Suma(int num1, int num2) {
		int suma = num1 + num2;
		return suma;
	}

	public int Resta(int num1, int num2) {
		int resta = num1 - num2;
		return resta;
	}

	public int Multiplicacion(int num1, int num2) {
		int mul = num1 * num2;
		return mul;
	}

	public int Division(int num1, int num2) {
		int div = num1 / num2;
		return div;
	}

}
